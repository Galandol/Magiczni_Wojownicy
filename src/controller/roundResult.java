package controller;

public class roundResult {

	private double attackersCurrentSpeed;
	private int enemyHP;
	
	public double getAttackersCurrentSpeed() {
		return attackersCurrentSpeed;
	}
	public void setAttackersCurrentSpeed(double attackersCurrentSpeed) {
		this.attackersCurrentSpeed = attackersCurrentSpeed;
	}
	public int getEnemyHP() {
		return enemyHP;
	}

	public void setEnemyHP(int enemyHP) {
		this.enemyHP = enemyHP;
	}
	

}
